package mx.com.superfish.aplicacion_lista;

public class Day {

    private String dia;
    private String descripcion;
    private String porcentaje_tiempo;

    public String getDia() {
        return dia;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getPorcentaje_tiempo() {
        return porcentaje_tiempo;
    }

    public void setPorcentaje_tiempo(String porcentaje_tiempo) {
        this.porcentaje_tiempo = porcentaje_tiempo;
    }
}
